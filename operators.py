num1 = 5
num2 = 9

# Arithmetic operators
print("num1 + num2",num1+num2)
print("num1 - num2",num1-num2)
print("num1 * num2",num1*num2)
print("num1 / num2",num1/num2)

# floor operator -> //
print(10//3)  # it will give me floor value only. without decimal value.

# exponential operator -> ** calculate the power
print(10 ** 3)

