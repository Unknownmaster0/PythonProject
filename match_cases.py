# match case == switch cases in C++/C

a = int(input('enter number b/w 1 to 5: '))

match a:
    case 1:
        print("I am 1.")
    case 2:
        print("I am 2.")
    case 3:
        print("I am 3.")
    case 4:
        print("I am 4.")
    case 5:
        print("I am 5.")
    case _:
        print("No, matching cases.This is default case.")



